function getGuaName(shang, xia) {
  return GUA_NAMES[`${xia}${shang}`] || `${TRIGRAMS[shang].name}${TRIGRAMS[xia].name}`;
}

function calcGua(n1, n2, n3) {
  const shangGua = n1 % 8 === 0 ? 8 : n1 % 8;
  const xiaGua = n2 % 8 === 0 ? 8 : n2 % 8;
  const dongYao = n3 % 6 === 0 ? 6 : n3 % 6;

  const ya = [...GUA_YAO[xiaGua], ...GUA_YAO[shangGua]];

  const hxNum = (ya[3] << 2) | (ya[2] << 1) | ya[1];
  const hsNum = (ya[4] << 2) | (ya[3] << 1) | ya[2];
  const hu = getGuaName(YAO_TO_GUA_NUM[hsNum], YAO_TO_GUA_NUM[hxNum]);

  const nya = [...ya];
  nya[dongYao - 1] = 1 - nya[dongYao - 1];
  const bsNum = (nya[5] << 2) | (nya[4] << 1) | nya[3];
  const bxNum = (nya[2] << 2) | (nya[1] << 1) | nya[0];
  const bian = getGuaName(YAO_TO_GUA_NUM[bsNum], YAO_TO_GUA_NUM[bxNum]);

  const fu = getGuaName(xiaGua, shangGua);

  const tiNum = dongYao <= 3 ? shangGua : xiaGua;
  const yongNum = dongYao <= 3 ? xiaGua : shangGua;
  const tiyongKey = `${tiNum}${yongNum}`;

  return {
    ben: getGuaName(shangGua, xiaGua),
    hu, bian, fu,
    shangGua, xiaGua, dongYao,
    tiNum, yongNum,
    ti: TRIGRAMS[tiNum].name,
    yong: TRIGRAMS[yongNum].name,
    tiyong: TIYONG_RELATIONS[tiyongKey] || `${TRIGRAMS[tiNum].name}→${TRIGRAMS[yongNum].name}`
  };
}

